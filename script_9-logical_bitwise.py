# 9. Logical and Bitwise Operations
"""Logical operations combine boolean expressions. Bitwise operations work on binary representations.
    Syntax:
        logical - and, or, not
        bitwise - &, |, ^, ~, <<, >>
"""
a, b = True, False
logical_and = a and b  # False

x, y = 5, 3
bitwise_and = x & y    # 1 (binary: 0101 & 0011)
